public class Date
{
    public static void main(String[] args) {
        String day = "Tuesday";
        String date = "10th";
        String month = " January ";
        int year = 2023;
        
        System.out.println("American Format: " + day + "," + month + date + ", " + year);
        System.out.println("European Format: " + day + month + date + " " + year);
    }
}
